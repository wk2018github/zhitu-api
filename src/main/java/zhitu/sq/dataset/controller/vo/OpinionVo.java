package zhitu.sq.dataset.controller.vo;

public class OpinionVo {
	
	public String auditTarget;
	public String auditCoverage;
	public String auditTotalFunds;
	
	public String getAuditTarget() {
		return auditTarget;
	}
	public void setAuditTarget(String auditTarget) {
		this.auditTarget = auditTarget;
	}
	public String getAuditCoverage() {
		return auditCoverage;
	}
	public void setAuditCoverage(String auditCoverage) {
		this.auditCoverage = auditCoverage;
	}
	public String getAuditTotalFunds() {
		return auditTotalFunds;
	}
	public void setAuditTotalFunds(String auditTotalFunds) {
		this.auditTotalFunds = auditTotalFunds;
	}
	 
	 
}
