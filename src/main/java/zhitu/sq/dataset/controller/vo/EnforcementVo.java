package zhitu.sq.dataset.controller.vo;



public class EnforcementVo {

	public String auditType;
	public String auditTime;
	public String auditTarget;
	public String auditItems;
	
	public String getAuditType() {
		return auditType;
	}
	public void setAuditType(String auditType) {
		this.auditType = auditType;
	}
	public String getAuditTime() {
		return auditTime;
	}
	public void setAuditTime(String auditTime) {
		this.auditTime = auditTime;
	}
	public String getAuditTarget() {
		return auditTarget;
	}
	public void setAuditTarget(String auditTarget) {
		this.auditTarget = auditTarget;
	}
	public String getAuditItems() {
		return auditItems;
	}
	public void setAuditItems(String auditItems) {
		this.auditItems = auditItems;
	}

	
}
